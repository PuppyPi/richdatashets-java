package rebound.richdatashets.lib.model;

public class RichdatashetStructureException
extends RuntimeException
{
	private static final long serialVersionUID = 1l;
	
	public RichdatashetStructureException()
	{
		super();
	}
	
	public RichdatashetStructureException(String message)
	{
		super(message);
	}
	
	public RichdatashetStructureException(Throwable cause)
	{
		super(cause);
	}
	
	public RichdatashetStructureException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
